package Ex_010_For_loop;

public class Lab087_forloop_Initialization_data_type {
    public static void main(String[] args) {
        for (long l=123546l; l<6; l++)// long can be used in the initialization part
        {
            System.out.println(l);
        }
        for(float f=5666f;f<56f; f++)//float can be used  in the intialization part
        {
            System.out.println(f);
        }
    }
}
