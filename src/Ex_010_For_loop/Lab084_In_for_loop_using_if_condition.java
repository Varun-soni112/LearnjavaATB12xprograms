package Ex_010_For_loop;

public class Lab084_In_for_loop_using_if_condition {
    public static void main(String[] args) {
        for(int adilage=0; adilage<18;adilage++)
        {
            if(adilage>15)
            {
                System.out.println("he takes the gifts");
            }
            else
            {
                System.out.println("no gifts");
            }
        }
    }
}
