package Ex_04_Operators;

public class Lab026_Arithmetic_operators {
    public static void main(String[] args) {
        /*

        Arithmetic operators
        +( Addition )
        - ( subtraction)
        * ( multiplication)
        / (Division)
        %( modulus)

         */
        int a= 30;
        int b= 4;
        float f= 5.0f;

        System.out.println(a+b);
        System.out.println(a+f);
        System.out.println(b-f);
        System.out.println(a/f);
    }
}
