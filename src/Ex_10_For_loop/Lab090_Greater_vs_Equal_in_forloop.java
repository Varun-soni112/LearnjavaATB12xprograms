package Ex_10_For_loop;

public class Lab090_Greater_vs_Equal_in_forloop {
    public static void main(String[] args) {
        for(int i=0; i<=10; i++)
        {
            System.out.println(i);// 0 to 10, 11 times
        }
        for(int i=1; i<=10; i++)

        {
            System.out.println(i);// 1 to 10, 10 times

        }
        for(int i=0; i<10; i++)
        {
            System.out.println(i); //0to9, 10 times

        }
    }
}
