package Task._18june_Corejava_forloop_whileloop.Forloop;

public class Task60_print_sum_of_first_10_natural_no {
    public static void main(String[] args) {
        int sum=0;
        for(int num=1; num<=10; num++)
        {
            sum = sum+num;
        }
        System.out.println(sum);
    }
}
